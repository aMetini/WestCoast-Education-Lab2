class Course {
    constructor (id, courseNumber, title, description, length, imageName, category, price) {
        this.id = id;
        this.courseNumber = courseNumber;
        this.title = title;
        this.description = description;
        this.length = length;
        this.imageName = imageName;
        this.category = category;
        this.price = price;
    }
}